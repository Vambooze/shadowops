		public class OverlayModel
		{
			public OverlayModel( string collection, string hashName, string textName, int gender, int overlayZone, int price ) {
				Collection = collection;
				HashName = hashName;
				TextName = textName;
				Gender = (Genders) gender;
				OverlayZone = (OverlayZones) overlayZone;
				Price = price;
			}

			public OverlayModel() {
			}

			public string Collection { get; }
			public string HashName { get; }
			public string TextName { get; }
			public Genders Gender { get; }
			public OverlayZones OverlayZone { get; }
			public int Price { get; }
		}

		/// <summary>
		///     Return overlay model
		/// </summary>
		/// <param name="id"></param>
		public static OverlayModel GetOverlayModel( int id ) {
			try {
				foreach( var collection in OverlayMasterTable ) {
					if( collection.Value.TryGetValue( id, out var overlayModel ) ) {
						return overlayModel;
					}
				}
			}
			catch( Exception ex ) {
				Log.Error( ex );
			}
			return null;
		}

